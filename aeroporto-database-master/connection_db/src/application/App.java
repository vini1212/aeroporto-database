    private void altAero_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altAero_buttonActionPerformed
        DefaultTableModel model = (DefaultTableModel) altTable.getModel();

        while (model.getRowCount() != 0) {
            model.removeRow(0);
        }
        AeroportoDao auxDao = DaoFactory.createAeroportoDao();
        List<Aeroporto> list = auxDao.findAll();

        for (Aeroporto obj : list) {
            model.addRow(new Object[]{obj.getId(), obj.getNomeCidade()});
        }

        Object aux = JOptionPane.showInputDialog("Escolha a opção que deseja alterar pelo ID: ");
        if (aux != null) {
            Aeroporto auxAero = auxDao.findById(aux.toString());
            altIdTextAero.setText(auxAero.getId());
            altNomeTextAero.setText(auxAero.getNomeCidade());
            altAero_frame.setVisible(true);
        }
    }//GEN-LAST:event_altAero_buttonActionPerformed

